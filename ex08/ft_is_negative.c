#include <unistd.h>

void	ft_putchar(char c);

void	ft_is_negative(int n)
{
	char	c;

	if (n >= 0)
	{
		c = 'P';
		ft_putchar(c);
	}
	else
	{
		c = 'N';
		ft_putchar(c);
	}
}
